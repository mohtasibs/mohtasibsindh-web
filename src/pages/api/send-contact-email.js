import nodemailer from 'nodemailer';

// Secure API key for authentication
const CONTACT_FORM_API_KEY = 'a4b7f20f6bcd914d0f85099';

export default async function handler(req, res) {
    if (req.method !== 'POST') {
        return res.status(405).json({ message: 'Only POST requests are allowed' });
    }

    // Authenticate the request using the API key
    if (req.headers['x-api-key'] !== CONTACT_FORM_API_KEY) {
        return res.status(403).json({ message: 'Unauthorized: Invalid API key' });
    }

    try {
        const transporter = nodemailer.createTransport({
            service: 'gmail',
            auth: {
                user: process.env.EMAIL_USER, // Sender's email address
                pass: process.env.EMAIL_PASS, // App-specific password
            },
        });

        // Extract form data from request
        const { name, email, subject, message } = req.body;

        const mailOptions = {
            from: `"Website Contact Form" <${process.env.EMAIL_USER}>`, // Sender info
            to: 'sindhmohtasib@gmail.com', // Recipient email
            subject: `Contact Form: ${subject}`, // Email subject
            text: `
                You have received a new contact form submission:

                Name: ${name}
                Email: ${email}
                Subject: ${subject}
                Message:
                ${message}
            `,
            html: `
                <div style="font-family: Arial, sans-serif; line-height: 1.6; color: #333;">
                    <h2 style="color: #007BFF;">Contact Form Submission</h2>
                    <p><strong>Name:</strong> ${name}</p>
                    <p><strong>Email:</strong> <a href="mailto:${email}">${email}</a></p>
                    <p><strong>Subject:</strong> ${subject}</p>
                    <p><strong>Message:</strong></p>
                    <div style="background: #f4f4f4; padding: 10px; border-radius: 5px;">${message}</div>
                    <footer style="margin-top: 20px; font-size: 0.9em; color: #555;">
                        <hr style="border: none; border-top: 1px solid #ddd;" />
                        <p>This email was automatically generated by <strong>Mohtasib Sindh Contact Form</strong>. Please do not reply.</p>
                    </footer>
                </div>
            `,
        };

        await transporter.sendMail(mailOptions);

        res.status(200).json({ message: 'Contact form email sent successfully!' });
    } catch (error) {
        console.error('Error sending contact form email:', error);
        res.status(500).json({ message: 'Failed to send email', error: error.message });
    }
}
